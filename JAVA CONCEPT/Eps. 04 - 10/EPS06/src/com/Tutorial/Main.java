// Eps.6 Pengenalan Print dan Alur Eksekusi
package com.Tutorial;

public class Main {
    public static void main(String[] args){
        // Println
        System.out.println("Baris ke-1");
        System.out.println("Baris ke-2");

        // Print
        System.out.print("Baris ke-1");
        System.out.print("Baris ke-2");

        // Print + \n
        System.out.print("Baris ke-1 \n");
        System.out.print("Baris ke-2 \n");

        // Printf
        System.out.printf("Baris ke-%d",1);
    }
}
