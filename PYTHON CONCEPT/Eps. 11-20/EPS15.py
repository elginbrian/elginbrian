#Eps. 15 Pengenalan String
# String single quote, double quote, hybrid
print('Hola')
print("Hello")
print('"Konnichiwa')

# Backslash untuk meng-include kan '
print('Hari Jum\'at')

# Backslash untuk meng-include kan \
print("c:\\user\\folder")

# Backslash sebagai Tab
print("satu\tdua")

# Backslash sebagai Enter
print("baris1\nbaris2")

# String literal atau raw
print(r"c:\jum'at\r\t")

# Multiline literal string
print("""
Day : 1
Hour : 3
""")

# Multiline literal string dan raw
print(r"""
Hola
c:\jum'at\r\t
""")