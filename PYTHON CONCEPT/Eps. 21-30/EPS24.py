# Eps. 23 For Loop
# Menggunakan List
angka = [0,1,2,3,4]
for angka in angka :
    print(f"angka sekarang adalah : {angka}")

# Menggunakan Range
angka = range(5)
for angka in angka :
    print(f"angka sekarang adalah : {angka}")

# Menggunakan String
datastr = "halo"
for huruf in datastr :
    print(huruf)