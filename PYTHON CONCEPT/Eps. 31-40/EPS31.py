# EPS.31 Operasi List
data = [1,2,5,4,22,5,1,2,1,8]
print(data)

# Menghitung banyak kemunculan data
data1 = data.count(1)
print(data1)

# Mencari posisi data / index
data = ["alpha","beta","gamma"]
